#define MOD 1000000007
LL expo(LL a,LL n)
{
    LL result = 1;
    while(n)
    {
        if((n&1))
            result = (result * a) % MOD;
        a = (a*a) % MOD;
        n >>= 1;
    }
    return result % MOD;
}   

