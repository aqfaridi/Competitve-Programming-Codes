class AddMultiply {
public:
    int arr[2000010];
    int ele[2000010];
	vector <int> makeExpression(int);
};

vector <int> AddMultiply::makeExpression(int y) {
    vector<int> x;
    int k = 0;
    for(int i=-1000;i<=1000;i++)
        for(int j=2;j<=1000;j++)
        {
            if(i==0 || i==1)    continue;
            arr[k] = i*j;
            ele[k] = i;
            k++;
        }
    for(int i=-1000;i<=1000;i++)
    {
        if(i == 0 || i== 1) continue;
        int idx = lower_bound(arr,arr+k,y-i) - arr;
        if(arr[idx] == (y-i))
        {
            int x1 = arr[idx]/ele[idx];
            int x2 = arr[idx]/x1;
            int x3 = i;
            
            x.push_back(x1);x.push_back(x2);x.push_back(x3);
            break;
        }
    }
        
    return x;
}


