class HappyLetterDiv2 {
public:
    int cnt[26];
	char getHappyLetter(string);
};

char HappyLetterDiv2::getHappyLetter(string str) {

    int l = str.length(),sum,ans=-1;
    memset(cnt,0,sizeof(cnt));
    for(int i=0;i<l;i++)
        cnt[str[i]-'a']++;
    for(int i=0;i<=25;i++)
    {
        sum = 0;
        for(int j=0;j<=25;j++)
        {
            if(i==j)    continue;
            sum += cnt[j];
        }

        if(cnt[i] > sum)
        {
            ans = i;
            break;
        }
    }

    if(ans == -1)   return '.';
    else    return (char)(ans+'a');

}


