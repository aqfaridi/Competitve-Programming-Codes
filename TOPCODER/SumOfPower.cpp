class SumOfPower {
public:
	int findSum(vector <int>);
};

int SumOfPower::findSum(vector <int> arr) {

    int sum = 0,term=1;
    int n = arr.size();
    for(int i=n-1;i>=0;i--,term++)
        sum += arr[i]*(i+1)*term;
    return sum;
}


