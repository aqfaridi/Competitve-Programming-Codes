class FIELDDiagrams {
public:
    long long mem[50][50][50];
	long long countDiagrams(int);
    long long solve(int,int,int);
};

long long FIELDDiagrams::countDiagrams(int fieldOrder) {
    
    memset(mem,-1,sizeof(mem));
    return solve(fieldOrder,1,0)-1;
}

long long FIELDDiagrams::solve(int idx,int blocks,int profile)
{
    if(idx == 0)   return 1;

    long long& res = mem[idx][blocks][profile];
    if(res != -1)   return res;
    res = 0;

    for(int i=blocks;i>=profile;i--)
        res += solve(idx-1,blocks+1,i);
    
    //trace2(idx,res); 
    return res;
}

