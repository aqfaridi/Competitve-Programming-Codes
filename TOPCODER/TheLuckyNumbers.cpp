#define MAX 1023

class TheLuckyNumbers {
public:
    long long lucky[MAX];
    int length;
	int count(int, int);
};

int TheLuckyNumbers::count(int a, int b) {
    long long num,n;
    for(int i=1;i<MAX;i++)
    {
        num = 0;
        n = i;
        while(n)
        {
            n-=1;
            num = num*10 + ((n%2==0)?4:7);
            n/=2;
        }
        
        lucky[i] = num;
    }
    sort(lucky,lucky+MAX);
    return (upper_bound(lucky,lucky+MAX,b) - lower_bound(lucky,lucky+MAX,a));
}


