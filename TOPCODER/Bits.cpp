	int p0 = 10;
	Bits * obj = new Bits();
	clock_t start = clock();
	int my_answer = obj->minBits(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
