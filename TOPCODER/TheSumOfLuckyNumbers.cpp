#define MAX 127
class TheSumOfLuckyNumbers {
public:
    int lucky[MAX];
    int dp[1000010],parent[1000010];
	vector <int> sum(int);
};

vector <int> TheSumOfLuckyNumbers::sum(int sum) {
 
    vector<int> v;
    int num,n;
    for(int i=1;i<MAX;i++)
    {
        num = 0;
        n = i;
        while(n)
        {
            n-=1;
            num = num*10 + ((n%2==0)?4:7);
            n/=2;
        }
                                                
        lucky[i] = num;
    }
    
    sort(lucky+1,lucky+MAX);
    memset(dp,31,sizeof(dp));
    dp[0] = parent[0] = 0;

    for(int i = 1;i<=sum;i++)
        for(int j=1;j<MAX;j++)
            if(i-lucky[j] >= 0)
                if(dp[i] > dp[i-lucky[j]]+1)
                {
                    dp[i] = dp[i-lucky[j]] + 1;    
                    parent[i] = i-lucky[j];
                }
            
    if(dp[sum] != 522133279) 
        while(sum)
        {
            v.push_back(sum - parent[sum]);
            sum = parent[sum];
        }
    return v;
}


